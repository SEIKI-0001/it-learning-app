-- ============================================================================
-- バッジゲート型ロードマップの進行状態を永続化する
-- ----------------------------------------------------------------------------
-- これまで checkpointProgress（獲得バッジ・クリアCP・最終問題履歴・欠片・天井）は
-- localStorage のみに保存され、サーバー復元だけの端末では全報酬が失われていた。
-- 可変構造のため jsonb でまるごと保存する（1ユーザー1行の user_progress に追加）。
-- 列 NULL のときは従来どおり localStorage ＋ 移行推定で復元されるため後方互換。
-- アクセスは既存方針どおり service role 経由の API Route に限定。
-- ============================================================================
alter table public.user_progress add column if not exists checkpoint_progress jsonb;
