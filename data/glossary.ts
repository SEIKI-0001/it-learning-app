import type {
  Difficulty,
  GlossaryTerm,
  TopicField,
} from "@/types/content";

type GlossaryInput = Omit<GlossaryTerm, "quiz"> & {
  difficulty?: Difficulty;
  wrongChoices?: [string, string, string];
};

const DEFAULT_WRONG_CHOICES: [string, string, string] = [
  "プロジェクトの日程を横棒で表す図",
  "売上と費用が同じになる会計上の点",
  "法律や社内ルールを守る活動全般",
];

function createGlossaryTerm(input: GlossaryInput): GlossaryTerm {
  const wrongChoices = input.wrongChoices ?? DEFAULT_WRONG_CHOICES;
  return {
    ...input,
    quiz: {
      id: `glossary-${input.id}-q1`,
      prompt: `「${input.term}」の説明として最も近いものはどれでしょう？`,
      choices: [
        { key: "A", text: input.oneLine },
        { key: "B", text: wrongChoices[0] },
        { key: "C", text: wrongChoices[1] },
        { key: "D", text: wrongChoices[2] },
      ],
      correctChoice: "A",
      explanation: input.examPoint,
      difficulty: input.difficulty ?? 1,
      choiceExplanations: {
        A: input.oneLine,
        B: "役割が違います。用語の分野と目的を見分けましょう。",
        C: "会計や管理の説明であり、この用語の中心ではありません。",
        D: "ルール遵守の説明であり、この用語そのものの説明ではありません。",
      },
      reviewTags: input.reviewTags,
    },
  };
}

